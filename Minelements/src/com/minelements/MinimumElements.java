public class MinimumElements {
public static void main(String[] args) {
	int [] arr = new int[] {30,87,4,26,8,5,91};
	int min = arr[0];
	for(int i =0;i<arr.length;i++) {
		int i= 0;
		if(arr[i]<min);
		min = arr[i];
		System.out.println("min elements in an given array:" +min);
		
	}
	
}
}
